// ── SVG generator ─────────────────────────────────────────────────────────────
export const ICON_SIZE = 17, ICON_PITCH = 8, ICON_PAD = 4, ICON_VB = 140;
const R_ON = 3.2;
const CENTER = (ICON_SIZE - 1) / 2;

// Per-dot delay (in seconds) given (x,y). One scheme used for ALL anim types:
// a diagonal+radial blend that produces a soft wave traveling through the dots
// without distorting the grid. Keeps the animation visually consistent across
// every icon while still feeling "alive".
function dotPhase(x: number, y: number): number {
  const dx = x - CENTER;
  const dy = y - CENTER;
  const radial = Math.sqrt(dx * dx + dy * dy) / Math.SQRT2 / CENTER; // 0..1
  const diag = ((x + y) / (2 * (ICON_SIZE - 1)));                    // 0..1
  return Number(((radial * 0.5 + diag * 0.5) * -1.6).toFixed(3));    // 0..-1.6s
}

export function generateSvgFromBitmap(
  bitmap: string[],
  color: string,
  palette: Record<string, string> = {},
): string {
  let halo = '', on = '';
  for (let y = 0; y < ICON_SIZE; y++) {
    for (let x = 0; x < ICON_SIZE; x++) {
      const cx = ICON_PAD + x * ICON_PITCH + ICON_PITCH / 2;
      const cy = ICON_PAD + y * ICON_PITCH + ICON_PITCH / 2;
      const ch = bitmap[y]?.[x] ?? '.';
      const isOn = ch !== '.' && ch !== ' ';
      if (isOn) {
        const c = palette[ch] ?? color;
        const d = dotPhase(x, y);
        const st = `--d:${d}s`;
        halo += `<circle class="halo" style="${st}" cx="${cx}" cy="${cy}" r="${R_ON + 1.6}" fill="${c}"/>`;
        on   += `<circle class="on"   style="${st}" cx="${cx}" cy="${cy}" r="${R_ON}" fill="${c}"/>`;
      }
    }
  }
  return `<svg viewBox="0 0 ${ICON_VB} ${ICON_VB}" xmlns="http://www.w3.org/2000/svg"><g>${halo}</g><g>${on}</g></svg>`;
}

// ── Cached SVG getter ─────────────────────────────────────────────────────────
const _cache = new Map<string, string>();

export function getCachedBrandIconSvg(key: string): string {
  if (_cache.has(key)) return _cache.get(key)!;
  const icon = BRAND_ICONS_MAP[key];
  if (!icon) return '';
  const s = generateSvgFromBitmap(icon.bitmap, icon.color, icon.palette);
  _cache.set(key, s);
  return s;
}

/** Build a merged palette: icon's built-in palette + user overrides. */
export function mergePalette(
  iconKey: string,
  customPalette?: Record<string, string>,
): Record<string, string> {
  const base = BRAND_ICONS_MAP[iconKey]?.palette ?? {};
  if (!customPalette) return base;
  return { ...base, ...customPalette };
}
